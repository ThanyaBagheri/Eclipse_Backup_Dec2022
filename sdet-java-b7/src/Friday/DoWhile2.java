package Friday;

public class DoWhile2 {
	public static void main(String[] args) {
		
		// Declaring and initialization expression
		int a = 1;
		
		// Do-while loop
		do {
			
			// Body of do-while loop
			// Print statement
			System.out.println("Good evening class!");
			
			// update expression
			a++;
		}
		
		// Test expression
		while (a < 6);
		
	}
}
