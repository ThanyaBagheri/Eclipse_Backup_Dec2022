package day21;

public class Review {
	public static void main(String[] args) {
		// while loop
		// do while loop

		// while loop vs do while loop
		// in while loop the condition goes first and then the body
		// in do while loop the body goes first
		// do while loop will have at lest one iteration, but while loop
		// can have 0 iterations.

		int num = 1;

		while (num <= 101) {
			System.out.println(num);
			num++;
		}
	}
}
